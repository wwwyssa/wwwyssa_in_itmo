SELECT "Н_УЧЕНИКИ"."ГРУППА" AS НОМЕР_ГРУППЫ,
       "Н_ЛЮДИ"."ИД" AS НОМЕР_СТУДЕРНТА,
       "Н_ЛЮДИ"."ФАМИЛИЯ" || ' ' ||  "Н_ЛЮДИ"."ОТЧЕСТВО" || ' '  ||"Н_ЛЮДИ"."ИМЯ" as ФИО,
       "Н_УЧЕНИКИ"."СОСТОЯНИЕ",
       "Н_УЧЕНИКИ"."ИД" AS номер
FROM "Н_ЛЮДИ"
         JOIN "Н_УЧЕНИКИ" ON "Н_УЧЕНИКИ"."ЧЛВК_ИД" = "Н_ЛЮДИ"."ИД"
WHERE "Н_УЧЕНИКИ"."НАЧАЛО" < DATE '2012-09-01'